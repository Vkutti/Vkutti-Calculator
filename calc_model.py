
def add(num1, num2):
    num1 = float(num1)
    num2 = float(num2)
    return num1 + num2


def multiply(num1, num2):
    num1 = float(num1)
    num2 = float(num2)
    return num1 * num2


def subtract(num1, num2):
    num1 = float(num1)
    num2 = float(num2)
    return num1 - num2


def divide(num1, num2):
    num1 = float(num1)
    num2 = float(num2)
    return num1 / num2


def exponent(num1, num2):
    num1 = float(num1)
    num2 = float(num2)
    return num1 ** num2







